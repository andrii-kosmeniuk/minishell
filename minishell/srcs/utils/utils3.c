int	alpha_numeric_underscore(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z') || (c == '_'))
		return (1);
	else
		return (0);
}
